package loops;

public class ForLoop {

	public static void main(String[] args) {
		// int counter;
		//we need to use i for the couner i++ i means iterator
		int counter;
		//in bracket initialization, condition, increment decrement
		for( counter =1; counter<=5; counter++)
		{
			//System.out.print("*");
			
			System.out.print(counter);
		}
		System.out.print(" "+counter);
		

	}

}
