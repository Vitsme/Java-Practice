package forLoop;

public class Practice {

	public static void main(String[] args) {
		//for Ascending Order
		
		for(int i=0; i<=10; i++)
		{
			System.out.println(i);
		}
		System.out.println("completed");
	
	}
}

class Practice1 {

	public static void main(String[] args) {
		//for Descending Order
		
		for(int i=10; i>=0; i--)
		{
			System.out.println(i);
		}
		System.out.println("completed");
	
	}
}
