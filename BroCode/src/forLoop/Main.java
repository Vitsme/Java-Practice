package forLoop;

public class Main {

	public static void main(String[] args) 
	{
		// for loop = executes a block of code a limited amount of times
		
		//for (int i=10; i>=10; i--)     //for reverse
		//instead of i-- use i-=2  for doubling
		for(int i=0; i<=10; i++)
		{
			System.out.println(i);
			//here also we can use i++ remove from for loop   //i-=2;
		}
		System.out.println("Happy new year!");
		

	}

}
